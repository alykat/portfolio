<p>AZCentral.com, The Arizona Republic's flagship website, was redesigned in summer 2000 to coincide with the site's fifth anniversary. The primary goals of the redesign were faster page loading times and a more uniform across-the-site look. A &quot;facelift&quot; of this design (shown above) was undertaken in Jan. 2002 with the aim of making the site easier to navigate and providing a bit more visual &quot;weight&quot; to the page.</p>
<p>For the site's 2004 redesign, I was contracted to work on the main index/subindex pages of the Entertainment section, the calendar results and detail pages and azcentral@night. The main site &quot;look&quot; already had been established in-house, and part of my job was to propogate that style into those inside pages.</p>

