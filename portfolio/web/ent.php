<h4>azcentral.com/The Arizona Republic / March 2000, April 2002</h4>

<p>&raquo; <a href="http://www.azcentral.com/ent/">View site</a> (external link)<br>
  &raquo; <a href="/portfolio/web/rep2000.jpg">View a screenshot of the 2000 design</a></p>

<p>The online version of The Rep, The Arizona Republic's weekly entertainment pull-out section, was redesign/relaunched in <a href="/portfolio/web/rep2000.jpg">March 2000</a> to coincide with the redesign of the print edition. The goals of this redesign were to update the look, feature more stories and better integrate our existing database resources, especially for events calendar listings. In <a href="http://www.azcentral.com/ent/">April 2002</a>, the section was renamed simply &quot;Entertainment Guide,&quot; separating it from The Rep print product, and was redesigned to bring its look more in line with that of the rest of azcentral.com.</p>
<p>This section won Best Entertainment Section in an Online Newspaper Service in the 2001 Editor &amp; Publisher EPpy Awards, beating out finalists washingtonpost.com and nytimes.com, and was a finalist in the same category in 2002.</p>

