            <h2>Students sacrifice sleep, warmth for dorm-room assignments</h2>
            <h4>State Press / Feb. 18, 1998</h4>

            <p>With dreams of scoring the &quot;perfect&quot; dorm room, about 
              20 McClintock Hall residents braved the cold and camped out Monday 
              night in front of the Student Services Building.</p>
            <p>Residence Life re-application for students wanting to live in the 
              same dorm but a different room began Tuesday morning and continues 
              through Thursday.</p>
            <p>The students, joined early Tuesday morning by other McClintock 
              residents, staked out their place in line in hope the of changing 
              from the dorm's &quot;A&quot; rooms to the larger &quot;B&quot; 
              rooms next year.</p>
            <p>&quot;It was like camping out for concert tickets, except that 
              a concert lasts five hours and housing is for two whole semesters,&quot; 
              Melissa Mayette, a pre-business major, said. &quot;That's why this 
              was important to us.&quot;</p>
            <p>Most of the early birds were placed in &quot;B&quot; rooms. Some 
              even organized suite arrangements with friends.</p>
            <p>&quot;B&quot; rooms are considered jewels for McClintock residents, 
              due to their scarcity and distribution usually only to returning 
              students. Over the past few years, it has become a McClintock tradition 
              for &quot;A&quot; room residents to camp out the night before re-registration 
              to make sure they snag a &quot;B&quot; room for the next year.</p>
            <p>Students arrived as early as Monday afternoon to begin camping 
              out. Mayette and her sister Jessica arrived at 4 p.m.</p>
            <p>This year's campers had to endure cold temperatures, which dipped 
              into the 30s Monday night. They were let into the Student Services 
              lobby around 5:30 a.m. Many used that opportunity to take a nap, 
              drawing curious stares from those who worked in the building as 
              they filed in to work.</p>
