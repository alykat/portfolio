<h2>Roadkill</h2>
            <h3>Phoenix-based clean-up service disposes of county's dead animals</h3>
            <h4>JRN 301 / December 4, 1998</h4>
            <p>Nikki Halvorson glanced briefly at the Phoenix address on the log 
              sheet, then tossed the clipboard on the dashboard of her Toyota 
              truck.</p>
            <p>Finding roadkill isn't always easy.</p>
            <p>Halvorson, a mother of two, works in an office, making calls and 
              talking with clients. She's also a sometime driver for Pets and 
              Animal Lovers Service, a company that contracts with city and county 
              governments and veterinary clinics all over Maricopa County to arrange 
              for the disposal of animals that have been put to sleep, have died 
              at home or have been left dead along the roads. PALS also accepts 
              calls from private individuals.</p>
            <p>The drivers pick up about 50 animals a day, countywide.</p>
            <p>&quot;We have drivers on seven days a week, 365 days a year &mdash; 
              even holidays,&quot; Halvorson said of the 11-year-old company founded 
              by Katherine Heuerman. &quot;We all pretty much try to take turns. 
              There's a rotation for driving, and if it comes up that (the holiday) 
              is your day to drive, then you get a chance at it &mdash; with holiday 
              pay and all.</p>
            <p>&quot;I'm the only one of the group who has children, so I kind 
              of get favoritism and don't have to work holidays.&quot;</p>
            <p>Concentrating on her route, Halvorson flipped through a map book 
              to look up the address of her first scheduled pickup of the afternoon. 
              &quot;Where we're going to now is a dog,&quot; she said.</p>
            <p>Pulling into a side street near Earll Drive and 67<sup>th</sup> 
              Avenue, Halvorson parked her truck next to the body of a black labrador. 
              The dog was stretched out on the warm pavement of a sidewalk and 
              looked like it was sleeping peacefully. Its body was not yet stiff 
              when Halvorson, wearing thick gloves, lifted it into the back of 
              her truck. There, she put the dog into a black plastic bag, marked 
              the bag with a special identification sticker and covered it with 
              a heavy tarp.</p>
            <p>&quot;There's two reasons that we contain them (in bags),&quot; 
              she said. &quot;The first is for identification purposes, obviously. 
              A kind of paper trail follows every peg. The second is kind of cleanliness. 
              It's not mandated that you contain a pet after it's been picked 
              up, but you don't want the blood or to have anyone else see it. 
              They're also contained for burial.</p>
            <p>&quot;Once in a while a little claw will come out, and the mess 
              proceeds.&quot;</p>
            <p>Back in the truck, she filled out an entry in the log sheet on 
              her clipboard.</p>
            <p>&quot;This paper log has all the calls listed,&quot; she said. 
              &quot;You write down the location, the type of animal, the weight 
              of the pet &mdash; of course, it's an approximate because we don't carry 
              scales &mdash; whether the pet was wearing tags, the time we received 
              the call and the time we picked it up.&quot;</p>
            <p>Halvorson drove back to the main road and stopped in the median, 
              flipping on her hazard lights before dodging traffic to get to a 
              cat that lay dead along the side of the road, not far from where 
              the dog had been. Rigor mortis had stiffened the cat's body, and 
              it stayed rigid as Halvorson picked it up by its hind legs to bring 
              it back to her truck.</p>
            <p>The cat was large and gray. The tires of a car had nearly flattened 
              it. Blood had dried around its mouth, which seemed frozen in a now-permanent 
              scream.</p>
            <p>After bagging, tagging and logging in the cat, Halvorson settled 
              back into the driver's seat and picked up her radio to call the 
              PALS office.</p>
            <p>&quot;Now I need to talk to the lead driver who's giving me the 
              calls so she knows they've been cleaned up,&quot; Halvorson said. 
              &quot;She can tell the city that at such and such a time the animal 
              was picked up.&quot;</p>
            <p>Halvorson filled out her log sheet as she reported the pickups 
              over the radio. Tossing her clipboard onto the truck's dashboard, 
              she looked into her mirrors to gauge traffic on the road and worked 
              her way into the lane, heading north. A few moments later, her pager 
              went off, listing a new assignment for her.</p>
            <p>Glancing at the message in the pager's display, she said, &quot;They're 
              going to keep us busy today.&quot;</p>
            <p>&nbsp;</p>
            <h3>Finding owners is tough</h3>
            <p>Almost none of the dead animals picked up has any kind of identification 
              to allow PALS to notify the pet's owners. &quot;A lot of people 
              are irresponsible,&quot; PALS owner Heuerman said.</p>
            <p>She added that her dog wears two tags.</p>
            <p>Halvorson, a cat owner, said her pet never goes outside unless 
              it's in a pet carrier.</p>
            <p>Neither the dog nor the cat that Halvorson picked up wore tags.</p>
            <p>&quot;The dog that I just picked up is a 40-pound lab/shepherd 
              mix,&quot; Halvorson said. &quot;He didn't look emaciated or underfed 
              or sick or anything, so I'd have to assume that at least someone 
              was feeding him.</p>
            <p>&quot;We can't find out who he belongs to because he doesn't have 
              a collar on. By standards, he's what we'd consider a stray, but 
              I would have to think, given his size, that he probably wasn't.&quot;</p>
            <p>People looking for their pets sometimes call PALS.</p>
            <p>&quot;There's times when someone will call, say, the same day we've 
              already been there, or maybe the next day, wanting to see their 
              pet,&quot; Halvorson said. &quot;Of course, I have to tell them 
              no. There was no identification on the pet, so of course we had 
              to bury it. It doesn't offer closure to the owners, but... I have 
              to tell them in their time of grief that if one tiny little tag 
              were there, there'd be no question (of who the pet belonged to).</p>
            <p>&quot;And of course we get all kinds of answers as to why (the 
              pets aren't) wearing tags. Generally 'the bath' is the best one. 
              That's the good one. And I'm sure that's probably true. I know my 
              dog, when she gets her bath done she's off running like the races.&quot;</p>
            <p>Stray animals and their offspring can account for much of the roadkill 
              PALS picks up.</p>
            <p>&quot;People don't take responsibility for their pets, and then 
              they multiply,&quot; Heuerman said.</p>
            <p>PALS doesn't deal with the disposal of all kinds of roadkill, though. 
              Under the Arizona Revised Statues, a driver can take home big game 
              animals that he may have hit accidentally, provided he gets a permit 
              before doing so: &quot;The carcass of a big game animal that has 
              been killed as a result of an accidental collision with a motor 
              vehicle on a maintained road may be possessed and transported by 
              the driver of the vehicle if the driver first obtains a big game 
              salvage permit issued by a peace officer&quot; (ARS 17-319).</p>
            <p>Most of PALS' calls relate to pets, although they have sometimes 
              been called to pick up horses and the dead pets of private owners. 
              Halvorson said that drivers will also pick up dead animals they 
              see in the road, including birds, even if those animals have not 
              yet been called in.</p>
            <p>PALS has contracts with most towns and cities in the county, along 
              with the county itself, to pick up dead animals. There are usually 
              two drivers out at a time, working from early morning until about 
              6 at night, responding to calls.</p>
            <p>Drivers will respond to reports phoned in to a city or county's 
              sanitation department. Each government entity will then forward 
              the calls within its own jurisdiction to PALS.</p>
            <p>&quot;We only pick up on county-maintained roads,&quot; said Dan 
              Schriek, a Maricopa County Sanitation Department supervisor. &quot;We 
              don't have anyone patrolling the streets or anything. We rely on 
              citizens to call in and report, say, a dead dog along the side of 
              the road on Highway 80 near Wickenburg. We contract out to PALS 
              for pickup.&quot;</p>
            <p>Animals picked up on these kinds of calls range from roadkill to 
              dead animals that people have placed in bags and set on their curbs. 
              PALS drivers usually only make pickups that have been assigned to 
              them, although they will pickup roadkill that hasn't been called 
              in yet &quot;because we'll just have to go back and pick it up later,&quot; 
              Halvorson said.</p>
            <p>PALS responds to most of its calls in Phoenix, which is the only 
              municipality in the county that offers residents free curbside pickup 
              of dead pets.</p>
            <p>&quot;The City of Phoenix offers a free pickup and disposal, which 
              is just a mass-burial at landfill at no charge to the resident as 
              long as they place it next to a city right-of-way &mdash; that could 
              be in an alley next to a dumpster or by a curb,&quot; Halvorson 
              said.</p>
            <p>PALS bills a city or a county for each pickup. The amount depends 
              on the contract it has with each entity. The City of Phoenix pays 
              less per pickup than Guadalupe, which may yield two or three pickups 
              per month, Heuerman said. However, because of the volume of pickups 
              PALS makes in Phoenix, that city gets a kind of &quot;bulk rate&quot; 
              in its contract.</p>
            <p>&quot;(Earnings from) a contract can range, in a given year, anywhere 
              from $10,000 to $200,000,&quot; Heuerman said.</p>
            <p>PALS also has contracts with veterinary clinics, employing a driver 
              to make the rounds Monday through Wednesday to pickup euthanized 
              animals.</p>
            <p>&quot;The vet driver doesn't have anything to do with the city 
              or the regular pickups,&quot; Halvorson said. &quot;He's got a route 
              that he follows every time.&quot;</p>
            <p>PALS also deals with the bodies of canine cops.</p>
            <p>&quot;When a canine is killed in the line of duty, we take care 
              of the cremations for the police department for free,&quot; Halvorson 
              said.</p>
            <p>Halvorson estimated that she drives at least 200 miles in a day, 
              and that the other driver might drive more than 300.</p>
            <p>&quot;When I first got here, I was always lost,&quot; she said 
              as she negotiated her way through Phoenix and turned onto Interstate-17. 
              &quot;So I've really gotten to know the city. Prior to this, I was 
              a school bus driver. That really helped, coming here, because I 
              got my bearings as to where I was and where I needed to be in the 
              city.</p>
            <p>&quot;Figuring out where you're going is probably the hardest part 
              of your job, because it's always changing, every hour. You constantly 
              have to re-route. Most of your job is driving around, routing, figuring 
              out the fastest way.&quot;</p>
            <p>&nbsp;</p>
            <h3>They're not just roadkill</h3>
            <p>Halvorson said that she and the people she works with definitely 
              consider themselves &quot;pet people.&quot; Halvorson owns a cat, 
              three fish and an eel. She also helps support a dog, paying for 
              food, veterinarian bills and other costs, while a friend houses 
              her.</p>
            <p>&quot;She's a great dane, but I couldn't keep her in my apartment,&quot; 
              Halvorson said. &quot;I just bought a house, but there's not much 
              yard.&quot;</p>
            <p>People at PALS look at their jobs from the perspective of pet owners.</p>
            <p>&quot;We treat these animals as though they might be ours or (might 
              belong to) someone we know,&quot; Halvorson said. &quot;Unfortunately, 
              when we go pick these (animals) up, people think they're just roadkill, 
              but they belong to someone, somewhere.</p>
            <p>&quot;It gives this cat a little dignity to come off the road; 
              that's how I see it.</p>
            <p>&quot;If that happened to my kitty, I'd hope that someone would 
              come along to take care of my kitty-cat for me.&quot;</p>
            <p>After about 15 minutes of whipping through Phoenix, Halvorson pulled 
              into another residential neighborhood, pausing for a moment to check 
              her log sheet for the correct address. Then she slowly drove down 
              the street, finally stopping in front of a house that was still 
              decorated for Halloween. A large cardboard skeleton was pinned to 
              the roof of the front porch, and streamers attached to the front 
              door blew gently in the wind. In front of the house, at the curb, 
              a large plastic garbage bag lay next to the trash bin. Inside it 
              was a female pit bull.</p>
            <p>&quot;On certain occasions, when someone has one in front of their 
              house, there might be a note or a little ditty or some flowers,&quot; 
              she said. &quot;Sometimes kids draw little pictures of the pet and 
              put it on the bag.&quot;</p>
            <p>Halvorson added that picking up pets in front of children can be 
              difficult.</p>
            <p>&quot;Once in a while there will be some kids sitting by a pet 
              (waiting for it to be picked up),&quot; she said. &quot;That's real 
              difficult because not only do they sometimes not know (what's going 
              on), but sometimes, when you're looking for the most efficient way 
              to take care of a pet, you're not able to kind of cuddle the pet 
              or pick it up real gently. And sometimes there might be a lot of 
              blood or weight involved.</p>
            <p>&quot;It's hard to be real discreet around a child because they 
              don't know you're being discreet. They just don't understand it. 
              I think they're probably the hardest. It would be much easier if 
              a parent or someone was there explaining to them what's going on, 
              but that's never happened to me.&quot;</p>
            <p>Halvorson stepped out of the truck and hefted the dog into the 
              back. She opened the bag to look at the dog, noting on her log.</p>
            <p>&quot;Not only was she contained, but she was double-contained,&quot; 
              Halvorson said. &quot;She was probably a family pet.&quot;</p>
            <p>Part of PALS' business is based on counseling the families about 
              their options after the death of a pet and being open about the 
              disposal process.</p>
            <p>Heuerman, who had to put her dog Duffy to sleep just after moving 
              to Phoenix, was, to some degree, traumatized by her experience in 
              cremating her pet.</p>
            <p>&quot;They wouldn't let me see her, they wouldn't let me see where 
              she was going. It took me three weeks to get her back,&quot; Heuerman 
              said. &quot;Then I finally was able to see where they took Duffy, 
              and I found out why they didn't want me to see it.</p>
            <p>&quot;I got into this because I didn't want others to go through 
              what I had gone through.&quot;</p>
            <p>Heuerman founded PALS on a part-time basis. The business began 
              to pickup, and she soon took up heading the business full time, 
              adding staff and expanding services.</p>
            <p>&quot;The real reward that I get (from this job) is the gratitude 
              I feel from the people after we've helped them (take care of their 
              pet),&quot; she said. &quot;It's such a rush.&quot;</p>
            <p>Halvorson said they encourage people to ask questions and to learn 
              everything about their options after a pet's death.</p>
            <p>&quot;Secrets make for bad feelings,&quot; she said. &quot;That's 
              why we've always had an open-door policy.</p>
            <p>&quot;Let people come in and see where you work and what you do 
              and how you work. We explain the process. If they have questions, 
              we're supposed to answer them.</p>
            <p>&quot;We may not be the service for you, and if we're not, that's 
              OK. I can explain the process to someone on the phone, spending 
              30 to 40 minutes explaining how things go. And at the end of the 
              conversation, I feel just as comfortable saying to them, 'If we 
              can't help you, there are services out there that can, and this 
              is where you need to go to get the help that you need.'&quot;</p>
            <p>Other services might be the Pet Grief Support Line, which Heuerman 
              helped found, and other agencies that deal with dead animals, such 
              as the Animal Cremation Service or the Arizona Pet Mortuary.</p>
            <p>PALS also allows pet owners who chose to have their pet cremated 
              to watch the cremation if they wish.</p>
            <p>&quot;We also offer a witness program where they can come in, if 
              they want their pet cremated, and watch so they know that their 
              pet is the only pet being cremated,&quot; Halvorson said. &quot;When 
              I first started here five years ago, we probably did one witness 
              a month; now we're doing four to five a week. There's a calling 
              for it.&quot;</p>
            <p>When helping grieving pet owners figure out their options, PALS 
              tries to humanize the process as much as possible, Halvorson said.</p>
            <p>&quot;We don't refer to their pets as just 'pets,' she added. &quot;We 
              make a point of asking what their pet's name is, because even if 
              they're deceased, they're still a family member and they'd like 
              to be referred to as 'Skippy' or 'Fluffy' or &mdash; my cat's name is 
              'Spaz.'</p>
            <p>&quot;When I called up and made arrangements when my mother passed 
              away, they never said 'the body,' 'the corpse,' whatever. They always 
              used her first name because even though she's deceased, she's still 
              a being. So we try to make a point of referring to their pet by 
              whatever their pet's name is. We just want to make them feel comfortable 
              because this is a difficult time.&quot;</p>
            <p>&nbsp;</p>
            <h3>It takes all kinds</h3>
            <p>Halvorson stopped to make her next pickup: a cat a front yard. 
              The dark gray animal was not in a bag. It was instead stretched 
              out on its side, about three feet from the curb.</p>
            <p>&quot;This cat right here, we would not normally remove it because 
              it's on private property,&quot; she said. &quot;But if I were to 
              call and say, 'You have to come out here and remove the pet,' they 
              would say to me, 'Well why can't you just come out here and get 
              it?'</p>
            <p>&quot;I'm going to go get it because it would make another trip 
              back here. My guess is that they didn't want to touch it, and that's 
              probably why it's still here.&quot;</p>
            <p>Halvorson picked up the cat by its haunches. Like the earlier cat, 
              it stayed in the same rigid position as it had when it was lying 
              in the grass. Its mangy fur didn't move at all in the breeze as 
              Halvorson put it in the back of the truck.</p>
            <p>PALS drivers have their share of odd experiences, from unusual 
              animals called in for pick-up to strange situations.</p>
            <p>&quot;Sometimes, the city or maybe a family member has called and 
              said that unfortunately, like, Mrs. Smith passed away and went unnoticed 
              for awhile, so then pets expired as well and they don't know what 
              to do,&quot; she said. &quot;Unfortunately, though, the county morgue 
              or your funeral home won't do anything with pets, so they call us. 
              I'll go in and remove the pets and take care of their disposition, 
              whether it be cremation or disposal or whatever.</p>
            <p>&quot;They send me in because I have a medical background, and 
              I've worked in the urgent care and trauma for a long time. I've 
              seen a lot of things, so smells and things like that don't bother 
              me like they do some of the other drivers. Steel stomach.&quot;</p>
            <p>Halvorson said the oddest pickup she's had to make was when &quot;I 
              went in for two dogs and came out with six. The reason it was so 
              odd was that the gentlemen (from the morgue) who were there to take 
              care of the dead woman were really odded out by the fact that there 
              were dead pets and that I could touch them. The dead woman was not 
              in good shape. We got to talking about the differences between my 
              job and their job. I told them I thought theirs was harder, but 
              they thought mine was harder.&quot;</p>
            <p>Halvorson said she got into this line of work to supplement her 
              income.</p>
            <p>&quot;Touching deceased animals isn't something you ever grow up 
              wanting to do,&quot; she added. &quot;It's especially difficult 
              the first week or so, thinking about it and seeing it.&quot;</p>
            <p>She came to Arizona from Minnesota for graduate school and because 
              she had family ties in the state. She had trouble finding a job 
              with a doctor's office or hospital, so she sought out other kind 
              of work to support herself and two daughters in the meantime.</p>
            <p>&quot;I had graduated college and was looking for a job and was 
              quickly running out of savings,&quot; Halvorson said. &quot;(PALS) 
              was offering a driver's position, and I called looking for some 
              part-time work until I found, of course, which medical office I 
              was going to work at.</p>
            <p>&quot;This was never meant to be a full-time job. I told Katherine 
              that when I started. I just wanted to pad my savings account a little 
              bit and then move on. But now, these people are like my family. 
              I guess you just get kind of attached to everybody. I know <i>I</i> 
              did.&quot;</p>
            <p>What started out as a temporary job has now been Halvorson's full-time 
              job for the past five years. She said she doesn't necessarily like 
              what she does, but she stays because she likes the sense of family 
              she has with her co-workers and the opportunities she has to help 
              others.</p>
            <p>&quot;I don't like having to call someone and tell them their pet's 
              dead or injured,&quot; she said. &quot;And of course, I don't <i>like</i> 
              picking up dead animals off the side of the road. The hardest part 
              is telling someone &mdash; especially someone who doesn't know &mdash; that 
              their pet is dead. It's really difficult when you call and they're 
              really excited because you've found their pet, and then you have 
              to say, 'Sorry, but it's not what you think' and then have to tell 
              them what happened. Unfortunately, <i>that's</i> my job.</p>
            <p>&quot;If we do have to leave messages in regard to a pet, we don't 
              say, like, 'Hi, I'm looking for John. Your dog is dead. Call us 
              back.'&quot;</p>
            <p>Halvorson stopped working as a regular driver two-and-a-half years 
              ago and took up work in the office. She still does pickups when 
              needed and trains new drivers.</p>
            <p>As she turned into another neighborhood to make another pickup, 
              this time of a dog, Halvorson said that her family, especially her 
              daughters, has been fairly accepting of her job.</p>
            <p>&quot;I think a lot of people are taken aback when they hear what 
              I do for a living,&quot; she said. &quot;Of course, I'm not really 
              a driver anymore. I think they're really shocked to find out that 
              this is what I do pretty much all day, just driving around the city. 
              There are small amounts of time when you're actually out of the 
              vehicle. You chase addresses all day.</p>
            <p>&quot;And of course they always want to know if you have a shovel. 
              Probably before this job I would have thought the same thing myself. 
              Of course, when they ask, I have to say no. I guess (using a shovel) 
              makes sense for a kitten or small animal, but can you see trying 
              to pickup a rottweiler with one?&quot;</p>
            <p>Halvorson drove slowly down the residential road, looking for the 
              right address.</p>
            <p>&quot;I believe this is a dog, in front of a mailbox, in front 
              of a home,&quot; she said. &quot;I don't know if it just happened 
              to be killed there, or maybe if it was their pet or something...</p>
            <p>&quot;Aha. There it is.&quot;</p>
            <p>The smell of the animals in the back of her truck drifted into 
              the cab when Halvorson stepped out to pickup the bagged animal lying 
              by the mailbox.</p>
            <p>&quot;Here in the wintertime, (the smell is) not quite as odiferous,&quot; 
              she said. &quot;In the summer, the heat tends to speed the (decomposition) 
              process up a bit, and it brings a real generous smell with it. It's 
              a smell that &mdash; and I know people don't believe me when I say this 
              &mdash; you absolutely get used to.&quot;</p>
            <p>Halvorson carried the animal to the back of her truck, opened the 
              bag and noted that it was a fox, not a dog. She noted it in her 
              log, then planned out her route to her next stop.</p>
            <p>&quot;Now I'm going to work my way out to the West Valley and then 
              make my way to the landfill,&quot; she said.</p>
            <p>&nbsp;</p>
            <h3>Adventures in Lower Buckeye</h3>
            <p>Halvorson drove south, out of the city, into farmland, rough roads 
              and cattle country.</p>
            <p>&quot;Once in a while I get 'em way, waaaay out there, and you 
              even need to pack a lunch,&quot; she said. &quot;A couple times 
              I had to go out to a little town just on the other side of Wickenburg. 
              It's on the edge of Maricopa County.</p>
            <p>&quot;The only thing bad in the country is that they don't mark 
              every street, and a lot of them don't go through. You spend a lot 
              of time looking for the ones that do.&quot;</p>
            <p>Halvorson's pager went off, and she paused to check it. &quot;South 
              of Lower Buckeye,&quot; she said. &quot;Somehow I think we're trapped 
              out here.&quot;</p>
            <p>She passed a building with a large lawn. A dog was playing in the 
              sprinklers.</p>
            <p>&quot;It all looks so perfect,&quot; she said. &quot;Now if we 
              just had a cow to tip, we could just call it a day.&quot;</p>
            <p>Finally, she turned into a residential area and found the right 
              address. A large garbage bag lay next to the mailbox. A car was 
              parked in front of the house, but no one came out when Halvorson 
              stopped.</p>
            <p>She stepped out of her truck and grunted lightly as she lifted 
              the bagged dog. Underneath the bag was a worn blue blanket.</p>
            <p>&quot;It's probably an instance where a child didn't want the dog 
              to be cold, so they put a blanket there,&quot; she said.</p>
            <p>Halvorson hefted the dog, which she estimated at between 75 and 
              100 pounds, into the back of the truck and opened the bag so she 
              could identify it. The dog had been bleeding. She noted that it 
              was likely a German shepherd.</p>
            <p>&quot;That dog was pretty heavy,&quot; she said as she drove out 
              of the neighborhood. &quot;I remember that when I was a driver and 
              did pickups all the time, 75 pounds &mdash; even 100 pounds &mdash; you really 
              didn't strain all that hard because you're so used to carrying it.&quot;</p>
            <p>Once she got back to the main road, Halvorson checked the address 
              listed on her pager and went about looking for her last pickup of 
              the day.</p>
            <p>&quot;This is a Salt River Project address, so it's probably not 
              even a good one,&quot; she joked. &quot;It's just a location to 
              trick me into thinking something's there.&quot;</p>
            <p>Turning onto a narrow dirt road along an SRP irrigation canal, 
              she said, &quot;We can only be on the canals when we actually have 
              a call out here,&quot; she said. &quot;This is considered private 
              property, and we could get in trouble for trespassing.&quot;</p>
            <p>As she drove along the canal, she looked for any sign of a dead 
              animal, bagged or otherwise, along the side of the canal and at 
              each gate.</p>
            <p>Finally, she spotted a large garbage bag containing, presumably, 
              an animal, next to the gate at the end of the canal. She stopped 
              to inspect the package, then carried the dog to the back of her 
              truck.</p>
            <p>Once she'd made all her stops for the day, Halvorson headed for 
              a county landfill northwest of Phoenix.</p>
            <p>&quot;(What happens to these animals) depends on their disposition 
              order,&quot; she said. &quot;Currently, the ones I have are all 
