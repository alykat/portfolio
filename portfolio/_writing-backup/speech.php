            <h2>Roger Wilkins speech</h2>
            <h4>JRN 420 / April 6, 2000</h4>

            <p>Americans must develop a better understanding of their collective 
              past to develop better race relations for the future, civil rights 
              activist Roger Wilkins said in a speech last night at ASU's Gammage 
              Auditorium.</p>
            <p>&quot;Reconstruction can only happen when we recognize that we all have 
              a common past &mdash; no matter how painful that past is,&quot; he said.</p>
            <p>The speech was the fifth annual A. Wade Smith Memorial Lecture 
              on Race Relations, established in memory of an ASU sociology professor 
              who died of cancer in 1994.</p>
            <p>Wilkins earned a Pulitzer Prize in 1973 for his involvement in 
              the Washington Post's Watergate coverage. He is on the board of 
              directors for the NAACP magazine &quot;Crisis,&quot; and presently teaches 
              history and American culture at George Mason University.</p>
            <p>In his speech, Wilkins said that the country's rapidly changing 
              racial demographic &mdash; he cited one projection as saying that in 
              2050, the U.S. may be 50 percent white and 50 percent minority &mdash; 
              is going to force a massive re-think of Americans' national identity.</p>
            <p>&quot;For a country that has always thought of itself as a white country, 
              to go through that kind of demographic change would be quote traumatic,&quot; 
              he said. &quot;The psyche of our country is going to undergo a vast and 
              profound change. And in order to secure a ... future, we need to 
              understand the roots of our past.&quot;</p>
            <p>Wilkins shared his own experience in accepting both his family's 
              history in slavery and the country's history of slavery.</p>
            <p>&quot;George Mason, James Madison, Thomas Jefferson, George Washington 
              &mdash; These men were not just slave owners. This country owes a huge 
              debt to that generation &mdash; not just to the white men, but everyone, 
              including the black people who fought and worked for the revolution. 
              It was an integrated revolution,&quot; he said.</p>
            <p>&quot;My studies into history have helped me not only to understand 
              the Founding Fathers, but also turn generations of shame over slavery 
              into pride ... After learning that blacks fought from Lexington 
              to Concord, no one can tell me that, in some deep and profound way, 
              I don't own this country...&quot;</p>
            <p>&quot;That's not to say that I don't hate slavery and the prejudice, 
              but we're wasting an awful lot of energy that we could use to make 
              things better ... we need to save our children.&quot;</p>
            <p>Wilkins called on teachers and intellectuals to be among the first 
              to help promote that awareness.</p>
            <p>&quot;For deep reconstruction, we need more knowledge of where we are 
              as a people than we have now,&quot; he said. &quot;For this reconstruction, 
              we <i>need</i> scholars, we <i>need</i> intellectuals.</p>
            <p>&quot;My only regret ... is that I was miseducated all along the way. 
              I didn't understand who I was and where I came from until I undertook 
              my own education. Our children cannot continue to be miseducated.</p>
            <p>&quot;We not only need to know where we came from, we also need to know 
              where we are.&quot;</p>

