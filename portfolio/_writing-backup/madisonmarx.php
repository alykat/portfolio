            <h2>James Madison vs. Karl Marx on the Subject of Class</h2>
            <h4>POS 442 / Dec. 6, 2000</h4>

            <p>In <i>The Communist Manifesto</i>, Karl Marx writes, "The history 
              of all hitherto existing society is the history of class struggles" 
              (Marx 473). And while Marx, one of the founders of modern socialist 
              thought, and James Madison, the primary writer of the U.S. Constitution, 
              may lie on opposite ends of the spectrum in terms of their socio-political 
              thinking, the issue of (economic) class plays a key role in each 
              man's political philosophy. Marx writes that a revolution is coming, 
              in which the oppressed working classes will overthrow the bourgeoisie 
              elite, who enjoy unfair advantages and whose obsession with capitalist 
              gains have led to the dehumanization of the workers beneath them. 
              One might say that Madison writes to prevent such a revolution, 
              drafting a government that, to some extent, encourages factionalization<a href="#_ftn1" name="_ftnref1" title="">[1]</a> 
              and helps minimize the risk of tyranny by the majority (the working 
              classes) against the minority (the propertied and capitalist elite). 
              This paper attempts to address some of the ways in which Marx and 
              Madison address class, with respect to assorted writings by Marx 
              (most notably <i>The Communist Manifesto</i>) and Madison's work 
              on <i>The Federalist Papers</i>.</p>
            <p>Marx and Madison come from different schools of thought. Madison 
              is a neoclassical liberal in the school of John Locke, Thomas Hobbes 
              and David Hume, believing that a social contract exists between 
              the people and those who govern them, but distrusting the wisdom 
              of the general public in making decisions that affect the whole 
              of society. Marx is more a follower of Georg Wilhelm Friedrich Hegel, 
              a Romantic-era philosopher who taught that through the course of 
              history, society is moving and evolving closer to the "truth." This 
              philosophy incorporates Plato's idea of perfect forms, with Truth 
              being that perfect form and society slowly tearing away at the layers 
              that make it obscure. Marx incorporated those ideas into his own 
              socialist philosophy, writing that the nature of society has evolved 
              over the course of history, and that socialism is the logical next 
              step.</p>
            <p>Given their differing philosophical backgrounds, then, Madison 
              and Marx have different views on the origin of "property," but not 
              on its relation to class. Both men see the presence or absence of 
              property as being central to class and class relations. For Madison, 
              property is, as taught by Locke, that which is "properly" one's 
              own, namely one's life and freedom. Locke, in his <i>Second Treatise 
              on Government</i>, writes that property would also include one's 
              estate, those lands that one had invested time and labor into developing. 
              However, Locke, referring mainly to land and food, cautioned that 
              one should not take more than one really needs, or else the rest, 
              which could have gone to someone else, would be spoiled and wasted. 
              Locke is ambivalent on the idea of money, acknowledging that it 
              can be saved and does not spoil, but does not outright endorse a 
              capitalist system. People enter into the social contract, agreeing 
              to be governed (or to participate in a particular system of government) 
              for the purpose of preserving their property (life, liberty and 
              estate) from the dangers of life in the wild, in man's "natural 
              state." Once entered into that social contract, a divide emerges 
              between those with private property (estate, wealth) and those without 
              &mdash; the "haves" and the "have nots" &mdash; leading to the first societal 
              factions. In <i>Federalist #10</i>, Madison emphasizes that the 
              preservation of property is government's first concern, for without 
              that protection, the social contract would be broken and chaos would 
              ensue, resulting in the "have nots," who form a majority, trying 
              to take from the "haves," who form a minority.</p>
            <blockquote> 
              <p class="credit">But the most common and durable source of factions 
                has been the various and unequal distribution of property. Those 
                who hold and those who are without property have ever formed distinct 
                interests in society. Those who are creditors, and those who are 
                debtors, fall under a like discrimination. A landed interest, 
                a manufacturing interest, a mercantile interest, a moneyed interest, 
                with many lesser interests, grow up of necessity in civilized 
                nations, and divide them into different classes, actuated by different 
                sentiments and views. The regulation of these various and interfering 
                interests forms the principal task of modern legislation and involves 
                the spirit of party and faction in the necessary and ordinary 
                operations of government. (Madison 124)</p>
            </blockquote>
            <p>That initial split between the "haves" and "have nots" leads to 
              a multitude of other splits within the population on economic and 
              ideological grounds, to the point where, ideally, the threat of 
              the landed being overrun by the landless has essentially been eliminated, 
              yet government can still function as a decision-making body despite 
              the extensive factionalization of the public. Madison saw safety 
              in numbers, writing that "...the greater security afforded by a 
              greater variety of parties, against the event of any one party being 
              able to outnumber and oppress the rest..." (Madison 128).</p>
            <blockquote> 
              <p class="credit">If a faction consists of less than a majority, 
                relief is supplied by the republican principle, which enables 
                the majority to defeat its sinister views by regular vote ... 
                When a majority is included in a faction, the form of popular 
                government, on the other hand, enables it to sacrifice to its 
                ruling passion or interest both the public good and the rights 
                of other citizens ... Either the existence of the same passion 
                or interest in a majority at the same time must be prevented, 
                or the majority, having such coexistent passion or interest, must 
                be rendered, by their number and local situation, unable to concert 
                and carry into effect schemes of oppression. (Madison 125-6)</p>
            </blockquote>
            <p>Madison, like most of the other Founding Fathers, favored a republican 
              system of government over a strictly democratic one, in part because 
              of its greater efficiency over a large area and in part because 
              it helped lessen the possibility of tyranny by the majority. Madison 
              felt that while the system devised under the Constitution couldn't 
              fully control the outbreak of political factions &mdash; indeed, Madison 
              said that factions were a necessary result of a free-thinking republic 
              &mdash; the government could work to control the effects of factionalization.</p>
            <p>Meanwhile, Marx, in his 1845 work "The German Ideology" (co-written 
              with Friedrich Engels), explains the evolution of property from 
              shared tribal land to the trafficking of slaves to the present industrial 
              system in which a minority of people own the majority of power, 
              money and property.</p>
            <blockquote> 
              <p class="credit">The first form of property ... is tribal property 
                ... [S]ince several tribes live together in one town, the tribal 
                property appears as State property, and the right of the individual 
                to is as mere "possession" which, however, like tribal property 
                as a whole, is confined to landed property only. Real private 
                property began with the ancients, as with modern nations, with 
                movable property. (Slavery and community) ... [T]ribal property 
                evolved through various stages ... to modern capital, determined 
                by big industry and universal competition, <i>i.e.</i>, pure private 
                property, which has cast off all semblance of a communal institution... 
                (Marx 187)</p>
            </blockquote>
            <p>He writes that tribalism gave way to serfdom, then to feudalism, 
              and finally to the present industrial system, with a stronger ruling 
              class emerging from each progressive state. In <i>The Communist 
              Manifesto</i>, he writes that the current bourgeois ruling class 
              is destined to fall because, unlike in previous systems, it leaves 
              little or no opportunity for a member of the lower working classes, 
              the proletariat, to rise above his position and ascend into the 
              ruling class himself.<a href="#_ftn2" name="_ftnref2" title="">[2]</a></p>
            <blockquote> 
              <p class="credit">Hitherto, every form of society has been based, 
                as we have already seen, on the antagonism of oppressing and oppressed 
                classes. But in order to oppress a class, certain conditions must 
                be assured to it under which it can, at least continue its slavish 
                existence ... The modern labourer ... instead of rising with the 
                progress of industry, sinks deeper and deeper below the conditions 
                of existence of his own class ... And here is becomes evident, 
                that the bourgeoisie is unfit any longer to the be the ruling 
                class in society ...ø because it is incompetent to assure an existence 
                to its slave within his slavery, because it cannot help letting 
                him sink into such a state, that it has to feed him, instead of 
                being fed by him. (Marx 483)</p>
            </blockquote>
            <p>The bourgeoisie sets itself up for failure because it cannot perpetuate 
              itself and, because it holds the proletariat in the same poor state, 
              it sets itself up for a revolt by the same people it relies upon 
              for its continued existence. After that revolution, according to 
              Marx, society cannot move backward to a past, less exploitive system, 
              because history has shown that the current bourgeois system is the 
              inevitable result. Instead, he believes that society must move forward 
              and embrace communism, which calls for an outright renunciation 
              of private property in favor of communal, shared property, a term 
              that includes both land, capital and the means of producing capital.</p>
            <p>Madison, in addition to favoring a republican form of government, 
              supported the independence theory of representation, in which elected 
              representatives in government are encouraged to act according to 
              the dictates of their conscience rather than to strictly adhere 
              to the wishes of their constituents. Madison writes in <i>Federalist 
              #10 </i>that such representatives' "whose enlightened views and 
              virtuous sentiments render them superior to local prejudices and 
              schemes of injustice..." (Madison 128). In <i>Federalist #63</i>, 
              Madison goes further, in defending the make-up of the Senate, to 
              say that independent representatives are just that: independent 
              of citizens' passions and prejudices, and better able to make rational 
              decisions than the "mob" might.</p>
            <blockquote> 
              <p class="credit">I shall not scruple to add that such an institution 
                [as the Senate] may be sometimes necessary as a defense to the 
                people against their own temporary errors and delusions ... [T]here 
                are particular moments in public affairs when the people, stimulated 
                by some irregular passion, or some illicit advantage, or minded 
                by the artful misrepresentations of interested may, may call for 
                measures which they themselves will afterwards be the most ready 
                to lament and condemn. In these critical moments, how salutary 
                will be the interference of some temperate and respectable body 
                of citizens, in order to check the misguided career and to suspend 
                the blow meditated by the people against themselves, until reason, 
                justice, and truth can regain their authority over the public 
                mind? (Madison 371)</p>
            </blockquote>
            <p>Madison, as well as fellow <i>Federalist Papers</i> author Alexander 
              Hamilton, also subscribed to the idea of a "natural aristocracy," 
              people of high civic virtue who are well-read and educated &mdash; generally, 
              the wealthy, who have the leisure and financial resources to become 
              so &mdash; such that they can speak better for the people, whose general 
              lack of education and fickle nature makes them less fit to make 
              important decisions for the country. Hamilton writes in <i>Federalist 
              #35</i> that the lower classes are more likely to defer to the judgment 
              of their "superiors."</p>
            <p>Because the state is essentially run by members of this "natural 
              aristocracy," and that members of that aristocracy tend to belong 
              to Marx's bourgeois class, Marx argues that the State is essentially 
              run by and <i>for</i> the bourgeoisie, neglecting the interests 
              of the oppressed proletariat (in order to preserve the bourgeoisie 
              way of life) and of the society as a whole. In <i>The Communist 
              Manifesto</i>, he writes, "The executive of the modern State is 
              but a committee for managing the common affairs of the whole bourgeoisie" 
              (Marx 475). In "The German Ideology," Marx writes that the bourgeoisie 
              also controls the State through financial means as well, being an 
              important source of tax money, creditors on the national debt, and 
              owners of the most powerful corporations in the country, all of 
              which are important factors in the country's continued economic 
              well-being.</p>
            <blockquote> 
              <p class="credit">By the mere fact that it is a <i>class</i> and 
                no longer an <i>estate</i>, the bourgeoisie is forced to organize 
                itself no longer locally, but nationally, and to give a general 
                form to its mean average interest. Through the emancipation of 
                private property from the community, the State has become a separate 
                entity, beside and outside civil society; but it is nothing more 
                than the form of organization which the bourgeois necessarily 
                adopt both for internal and external purposes, for the mutual 
                guarantee of their property and interests. (Marx 187)</p>
            </blockquote>
            <p>For Marx, the control of national power and capital as well as 
              estate makes the bourgeoisie a class unto itself &mdash; and an oppressive 
              one at that, concerned, for the most part, with maintaining and 
              expanding their power and capital, but usually not allowing the 
              proletariat to share in their success. Marx goes on to write that 
              the assertion that the State's main purpose is the protection of 
              private property is misguided, if not outright propagandist: "The 
              most perfect example of the modern State is North America. The modern 
              French, English and American writers all express the opinion that 
              the State exists only for the sake of private property, so that 
              this fact has penetrated into the consciousness of the normal man" 
              (Marx 187). Marx argues that government should be responsible for 
              more than just citizens' private property, but for their continued 
              well-being as well (life and liberty, <i>as well</i> as property).</p>
            <p>Marx writes in <i>The Communist Manifesto </i>that the proletariat 
              will slowly begin to organize itself, stalling at times because 
              of political infighting, but continuing to develop until it is fully 
              realized as a political party/class. It will try to exert its influence 
              in government and politics, capitalizing on splits within the bourgeoisie 
              to achieve reform for the working class (Marx 481). Once the party 
              is ready, it will use its power as representative of the majority 
              to take over completely. Marx's plan for a democratic takeover of 
              government by the proletariat plays to Madison's fears of majority 
              tyranny, and, indeed, his comments are not at all reassuring to 
              lovers of property &mdash; or even of democratic government.</p>
            <blockquote> 
              <p class="credit">[T]he first step in the revolution by the working 
                class, is to raise the proletariat to the position of ruling class, 
                to win the battle of democracy. The proletariat will use its political 
                supremacy to wrest, by degrees, all capital from the bourgeoisie, 
                to centralise all instruments of production in the hands of the 
                State, <i>i.e.</i>, of the proletariat organized as the ruling 
                class; and to increase the total of productive forces as rapidly 
                as possible. Of course, in the beginning, this cannot be effected 
                except by means of despotic inroads on the rights of property, 
                and on the conditions of bourgeois production. (Marx 490)</p>
            </blockquote>
            <p>Marx adopts a "the ends justify the means" type of credo for this 
              stage in the proletariat revolution, advocating the stripping of 
              privilege from the bourgeoisie and possibly bringing about a kind 
              of stigma or oppression against the former ruling class. He calls 
              for the complete elimination of personal property, as private property 
              is the foundation of class divisions, and bringing society full-circle 
              to the tribal/communal system of shared property.</p>
            <blockquote> 
              <p class="credit">[M]odern bourgeois private property is the final 
                and most complete expression of the system of producing and appropriating 
                products, that is based on class antagonisms, on the exploitation 
                of the many by the few.</p>
              <p class="credit">"In this sense, the theory of the Communists may 
                be summed up in the single sentence: Abolition of private property.</p>
              <p class="credit">...Do [we] mean the property of the petty artisan 
                and of the small peasant, a form of property that preceded the 
                bourgeois form? There is no need to abolish that; the development 
                of industry has to a great extent already destroyed it, and is 
                still destroying it daily. (Marx 484-5)</p>
            </blockquote>
            <p>Marx's intent is to completely eliminate the multi-party system, 
              writing, "Political power, properly so called, is merely the organised 
              power of one class for oppressing another" (Marx 490). Marx instead 
              calls for the formation of a single unified party, concerned with 
              representing the needs of everyone. "In place of the old bourgeois 
              society, with its classes and class antagonisms, we shall have an 
              association, in which the free development of each is the condition 
              for the free development of all" (Marx 491). Essentially Marx tries 
              to eliminate the threat of "tyranny by the majority" by assuming 
              everyone is part of the minority. However, while Marx's one-party 
              system purports to be representative of all, concerns may be raised 
              that dissenting opinions will be heard or respected at all.</p>
            <p>In respect to property, class and the role of government, the ideologies 
              of Madison and Marx are almost totally at odds, with their calls 
              for rule by the people, albeit in different forms, being the only 
              thing that they might at all agree on.</p>
