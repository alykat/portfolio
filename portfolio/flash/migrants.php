<p>The summer of 2001 was notable for a marked increase in the number of people dying of exposure while trying to cross the U.S.-Mexico border in rural areas of Arizona. &quot;Dying to Work,&quot; a joint project of The Arizona Republic, azcentral.com and KPNX Channel 12, aimed to explore the &quot;human side&quot; of illegal immigration along the Mexican border, looking into why people cross the border and what they do once they get to the United States. I was part of the team that put the online site together, doing the site design and combining a series of graphics from the Newsroom art department into an interactive Flash format.</p>
<p>The project (print and online together) won First Place: Project Enterprise in the 2001 Best of Gannett Awards. The online component won First Place: Online Project Enterprise in the 2001 Best of the West Awards.</p>

