<p>&raquo; <a href="http://www.azcentral.com/news/census/census.html" onclick="window.open(this.href,'census','width=625,height=280,scrollbars=no,toolbar=no,directories=no,location=no,menubar=no,status=no'); return false">View project</a> (external link)</p>

<p>This project served as both my honors thesis at Arizona State University and as a useful multimedia piece to complement Arizona Republic coverage of the Census. The package was divided into three sections: an overview of demographic changes across the U.S. and in Arizona, an explanation of why Census data are important, and a search-by-zipcode function to allow users in Maricopa County to compare 1990 and 2000 Census results. The latter feature involved processing Census data in Microsoft Excel and Access, then aggregating the data from Census blocks to zip codes in ArcView. I finished as much of the package as I could in the month leading up to the Census data release. When the Census Bureau released Arizona's data, I worked overnight to finish the package in time to have it up the next morning, to accompany Arizona Republic stories related to the data release.</p>

