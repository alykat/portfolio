<p>Using original graphics created by the newsroom art department, I created (and updated, on a daily basis) an animated map showing the progress of the devastating &quot;Rodeo-Chediski&quot; fire in northeastern Arizona. The other features of this infographic &mdash; explainers about crown fires and fire-generated weather &mdash; are also Flash-ized versions of graphics originally created for print.</p>
<p>These Flash elements were part of a larger convergence special report 
called &quot;Runaway Inferno.&quot;  The project was a finalist for 
&quot;Most Innovative Use of Digital Media: News Event Coverage 
(Circulation over 250,000)&quot; in the 2003 Digital Edge Awards, 
sponsored by the Newspaper Association of America. &quot;Runaway 
Inferno&quot; also won second place for &quot;Creative Use of the Online 
Medium&quot; in the 2002 Arizona Press Club Awards.</p>

